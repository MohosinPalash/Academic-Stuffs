SET SERVEROUTPUT ON;

-- DEFAULT LOOP

DECLARE
	NUM int := 0;

BEGIN
	LOOP
		NUM := NUM + 1;
		DBMS_OUTPUT.PUT_LINE(NUM);
		IF NUM = 5 THEN
			EXIT;
		END IF;
	END LOOP;
END;
/

-- DEFAULT LOOP [EXIT CONDITION]

DECLARE
	NUM int := 0;

BEGIN
	LOOP
		NUM := NUM + 1;
		DBMS_OUTPUT.PUT_LINE(NUM);
		EXIT WHEN NUM = 5;
	END LOOP;
END;
/

-- WHILE LOOP

DECLARE
	NUM int := 0;

BEGIN
	WHILE NUM < 3
	LOOP
		NUM := NUM + 1;
		DBMS_OUTPUT.PUT_LINE(NUM);
	END LOOP;
END;
/

-- FOR LOOP

DECLARE
	NUM int := 7;

BEGIN
	FOR i IN 1..NUM 
	LOOP
		DBMS_OUTPUT.PUT_LINE(i);
	END LOOP;
END;
/